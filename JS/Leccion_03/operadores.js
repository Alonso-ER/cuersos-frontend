//Suma
let a = 3, b = 2;
let z = a + b;
console.log("Resultado de la suma = " +z);
//Resta
let y = a - b;
console.log("Resultado de la resta = " +y);
//Multiplicación
let x = a * b;
console.log("Resultado de la multiplicación = " +x);
//División
let w = a / b;
console.log("Resultado de la divición = " +w);
//Modulo
let u = a % b;
console.log("Resultado del Modulo (Residuo de la división) = " + u);
//Exponenciacion
let v = a ** b;
console.log("Resultado del exponente = " +v);
// Incremento
let q = ++a ;
console.log("El resultado del incremento = "+q);
// Pre-incremento (el operador ++ antes de la variable)
let t = b++
console.log("El resultado del incremento = "+t + " "+ b);
// Post-incremento (el operador ++ despues de la variable)

// Decremento
let c = 3, d = 2;
let p= --c;
console.log(p);
console.log(c);
let f = d--;
console.log(d);
console.log(f);